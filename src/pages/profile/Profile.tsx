import { logout } from "@/redux/slices/userSlice";
import { RootState } from "@/redux/store";
import { clearUserPreferences } from "@/services/CookieManager";
import { logoutUser } from "@/services/Logout";
import { FC } from "react";
import toast from "react-hot-toast";
import { useDispatch, useSelector } from "react-redux";
import { useNavigate } from "react-router-dom";

const Profile: FC = () => {
    const dispatch = useDispatch()
    const navigate =  useNavigate()
    const user = useSelector((state: RootState) => state.user);

  const handleLogout = async() => {
    const response = await logoutUser()
     if(response){
      clearUserPreferences()
      dispatch(logout())
      toast.success(response.data.message)
      navigate('/login')
     }
  };

  return (
    <div className="h-[92vh] bg-gray-100 flex items-center justify-center  dark:bg-gray-400">
      <div className=" shadow-lg rounded-lg w-80 p-6 bg-background text-foreground dark:bg-card dark:text-card-foreground">
        {/* Profile Card */}
        <div className="flex justify-center mb-4 ">
          <img
            src="https://via.placeholder.com/100" // Replace with your actual profile image URL
            alt="Profile"
            className="w-24 h-24 rounded-full border-4 border-indigo-500"
          />
        </div>
        <div className="text-center">
          <h2 className="text-2xl font-semibold text-gray-100">{user?.name}</h2>
          <p className="text-sm text-gray-500">Software Engineer</p>
          <p className="text-sm text-gray-400 mt-2">{user?.email}</p>
        </div>

        {/* Logout Button */}
        <div className="mt-6 text-center">
          <button
            onClick={handleLogout}
            className="w-full bg-red-500 text-white py-2 rounded-lg hover:bg-red-600 focus:outline-none dark:bg-red-900"
          >
            Logout
          </button>
        </div>
      </div>
    </div>
  );
};

export default Profile;
